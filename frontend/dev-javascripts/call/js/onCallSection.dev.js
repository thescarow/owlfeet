function changeHeightAndWidthOfOnCallUserBoxInMainView(
  mainViewWidth,
  mainViewHeight
) {
  let onCallUserBoxs = [
    ...onCallMainViewUserBoxContainer.getElementsByClassName("on-call-user-box")
  ]

  if (mainViewWidth === undefined) mainViewWidth = parseFloat(mainViewCss.width)

  if (mainViewHeight === undefined)
    mainViewHeight = parseFloat(mainViewCss.height)

  let noOfUsers = onCallUserBoxs.length

  let column = Math.ceil(Math.sqrt(noOfUsers))
  let percent = 100 / column - 1

  let bestHeightPercent =
    (mainViewWidth / mainViewHeight) * (1 / bestScreenWidthRatio) * percent

  if (column <= 3) {
    if (noOfUsers <= column * (column - 1)) {
      onCallUserBoxs.forEach(userBox => {
        userBox.style.width = percent + "%"
        userBox.style.height = bestHeightPercent + "%"
        userBox.style.maxHeight = 100 + "%"
        userBox.style.maxWidth = percent + "%"
      })
    } else {
      onCallUserBoxs.forEach(userBox => {
        userBox.style.width = percent + "%"
        userBox.style.height = bestHeightPercent + "%"
        userBox.style.maxHeight = 100 + "%"
        userBox.style.maxWidth = bestScreenWidth / column + "px"
      })
    }
  } else {
    onCallUserBoxs.forEach(userBox => {
      userBox.style.width = percent + "%"
      userBox.style.height = bestHeightPercent + "%"
      userBox.style.maxHeight = 100 + "%"
      userBox.style.maxWidth = bestScreenWidth / column + "px"
    })
  }
}

